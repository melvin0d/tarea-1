package cuartel;

public class Policia {
    private String nom;
    private String ran;



    public void setNombre(String n) {
        this.nom = n;
    }

    public String getNombre() {
        return this.nom;
    }

    public void setRango(String rango) {
        this.ran = rango;
    }

    public String getRango() {
        return this.ran;

    }
}